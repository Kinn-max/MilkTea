package model;

public class Suger {
	private String codeSuger;
	private String nameSuger;
	
	public Suger() {
		
	}
	public Suger(String codeSuger, String nameSuger) {
		super();
		this.codeSuger = codeSuger;
		this.nameSuger = nameSuger;
	}
	public String getCodeSuger() {
		return codeSuger;
	}
	public void setCodeSuger(String codeSuger) {
		this.codeSuger = codeSuger;
	}
	public String getNameSuger() {
		return nameSuger;
	}
	public void setNameSuger(String nameSuger) {
		this.nameSuger = nameSuger;
	}
	

}
